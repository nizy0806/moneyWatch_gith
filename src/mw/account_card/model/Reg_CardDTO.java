package mw.account_card.model;

public class Reg_CardDTO {
	
	private int num;
	private String id;
	private String card_company;
	private String card_name;
	private String card_nickname;
	private String account_company;
	private String account_num;
	private String balance;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCard_company() {
		return card_company;
	}
	public void setCard_company(String card_company) {
		this.card_company = card_company;
	}
	public String getCard_name() {
		return card_name;
	}
	public void setCard_name(String card_name) {
		this.card_name = card_name;
	}
	public String getCard_nickname() {
		return card_nickname;
	}
	public void setCard_nickname(String card_nickname) {
		this.card_nickname = card_nickname;
	}
	public String getAccount_company() {
		return account_company;
	}
	public void setAccount_company(String account_company) {
		this.account_company = account_company;
	}
	public String getAccount_num() {
		return account_num;
	}
	public void setAccount_num(String account_num) {
		this.account_num = account_num;
	}
	public String getBalance() {
		return balance;
	}
	public void setBalance(String balance) {
		this.balance = balance;
	}


	
	
	
	
}
